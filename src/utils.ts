export const log = {
  info: (message: string) => {
    // eslint-disable-next-line no-console
    console.info(message);
  },
  warn: (message: string) => {
    // eslint-disable-next-line no-console
    console.warn(message);
  },
  error: (message: string) => {
    // eslint-disable-next-line no-console
    console.error(message);
  },
};
